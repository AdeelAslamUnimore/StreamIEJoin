package com.baselinealgorithm.chainbplusandcss;

public class TupleModel {
    private int tuple;
    private int id;

    public TupleModel(int tuple, int id) {
        this.tuple = tuple;
        this.id = id;
    }

    public int getTuple() {
        return tuple;
    }

    public void setTuple(int tuple) {
        this.tuple = tuple;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
